        private void B_CHANGE_PERSON_ButtonClick(object pSender, EventArgs pEventArgs)
        {
            if (iConv.ISNull(W_APPROVAL_STEP_CODE.EditValue) == String.Empty)
            {
                MessageBoxAdv.Show(isMessageAdapter1.ReturnText("EAPP_90004", string.Format("&&FIELD_NAME:={0}", Get_Edit_Prompt(W_APPROVAL_STEP_NAME))), "Warning", MessageBoxButtons.OK, MessageBoxIcon.Warning);
                W_APPROVAL_STEP_NAME.Focus();
                return;
            }

            if (iConv.ISNull(W_PERSON_ID.EditValue) == String.Empty)
            {
                MessageBoxAdv.Show(isMessageAdapter1.ReturnText("EAPP_90004", string.Format("&&FIELD_NAME:={0}", Get_Edit_Prompt(W_PERSON_NAME))), "Warning", MessageBoxButtons.OK, MessageBoxIcon.Warning);
                W_PERSON_NAME.Focus();
                return;
            }
            if (iConv.ISNull(W_NEW_PERSON_ID.EditValue) == String.Empty)
            {
                MessageBoxAdv.Show(isMessageAdapter1.ReturnText("EAPP_90004", string.Format("&&FIELD_NAME:={0}", Get_Edit_Prompt(W_NEW_PERSON_NAME))), "Warning", MessageBoxButtons.OK, MessageBoxIcon.Warning);
                W_NEW_PERSON_NAME.Focus();
                return;
            }

            IDC_CHANGE_PERSON_ALL.ExecuteNonQuery();
            String vSTATUS = iConv.ISNull(IDC_CHANGE_PERSON_ALL.GetCommandParamValue("O_STATUS"));
            String vMESSAGE = iConv.ISNull(IDC_CHANGE_PERSON_ALL.GetCommandParamValue("O_MESSAGE"));
            if(vSTATUS == "F")
            {
                MessageBoxAdv.Show(vMESSAGE, "Error", MessageBoxButtons.OK, MessageBoxIcon.Error);                
            }
            else
            {
                MessageBoxAdv.Show(vMESSAGE, "Infomation", MessageBoxButtons.OK, MessageBoxIcon.Information);
                this.DialogResult = DialogResult.OK;
                this.Close();
            } 
        }

